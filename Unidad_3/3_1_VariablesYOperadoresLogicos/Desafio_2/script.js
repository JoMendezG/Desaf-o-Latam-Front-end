let num1 = prompt ("Ingresa un número");
let num2 = prompt ("Ingresa otro número");

document.write ("Tus números son: " + num1 + " y " + num2 );
document.write("<br>");
document.write ("La suma es: ");
document.write (parseInt(num1)+parseInt(num2));
document.write("<br>");
document.write ("La resta es: ");
document.write (parseInt(num1)-parseInt(num2));
document.write("<br>");
document.write ("La multiplicación es: ");
document.write (parseInt(num1)*parseInt(num2));
document.write("<br>");
document.write ("La division es: ");
document.write (parseInt(num1)/parseInt(num2));
document.write("<br>");
document.write ("El módulo es: ");
document.write (parseInt(num1)%parseInt(num2));